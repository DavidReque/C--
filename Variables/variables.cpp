#include <iostream>
#include <string>

int main() {
    // Ejemplo de diferentes tipos de variables en C++

    // Variable entera para almacenar números enteros
    int numeroEntero = 56;

    // Variable de tipo float para almacenar un número decimal de precisión simple
    float numeroDecimal = 0.56;

    // Variable de tipo double para almacenar un número decimal de precisión doble
    double numeroDecimalDoble = 1.74;

    // Variable booleana para almacenar verdadero o falso
    bool verdaderoFalso = true;

    // Variable de tipo carácter para almacenar una sola letra
    char miCaracter = 'D';

    // Variable de tipo cadena de texto
    std::string miCadena = "David";

    // Imprimir un saludo y concatenar una cadena de texto
    std::cout << "¡Hola, mundo! Mi nombre es " << miCadena << std::endl;

    return 0;
}
